
// single loop
for (let first_numb = 0; first_numb < 2; first_numb++) {
    console.log(first_numb)
}

for (let second_numb = 0; second_numb < 10; second_numb++) {
    console.log(second_numb)
}

// nested loop
for (let first_numb = 0; first_numb < 2; first_numb++) {
    for (let second_numb = 0; second_numb < 10; second_numb++) {
        console.log(`${first_numb},${second_numb}`)
    }
}



